                            For many centuries however, the only tea that was consumed was dried green tea leaves, which originated in China. Black tea was yet to be discovered. Until 850 AD tea was also prepared by simply boiling the tea leaves in water, not through today’s process of brewing. Traditionally there were three methods through which tea was prepared:<br><br>

                            Boiling Tea – Classical Period<br>
                            Whisking Tea – Romantic Period<br>
                            Brewing Tea – Naturalistic Period<br><br>

                            <b>The Classical Period</b><br>
                            This period in the history of tea lasted till about 850 AD. The tea leaves were first steamed and then ground with a pestle and mortar, then mixed with rice, ginger, salt, orange peel, spice, milk and onions and eventually boiled. The resulting concoction was a syrupy mixture of leaves. This form of tea bares a resemblance to the tea served with yak butter in Tibet today. During this period tea was also simply boiled and served.<br><br>

                            <b>The Romantic Period</b><br>
                            During the Romantic period, tea was “whisked” – a method made popular in the time of the Song dynasty (960-1279AD). The dried unfermented leaves were ground into a fine powder, after which it was added to boiling water and whisked into a thick sauce, with the aid of a bamboo brush. This form of tea preparation is carried out with much flair even today at the world renowned Japanese Tea Ceremonies.<br><br>

                            <b>The Naturalistic Period</b><br>
                            Tea “whisking” was replaced by “brewing” during the Chinese “Ming Dynasty” – the method of preparation that is practiced throughout the world today. Black tea also surpassed green tea in popularity during the Naturalistic Period.<br><br>
