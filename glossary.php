                                                <p>
                                                    CTC - Cut, Tear and Curl describes a machine, which literally cuts, tears and curls the withered leaf, breaking the leaf veins. This releases the juices or enzymes of the leaf and completes the second stage of manufacture. Today, the term CTC, or Unorthodox tea is applied to all types of manufacture other than Orthodox. It is used in the second stage of manufacture where the tea leaves are broken into particles before fermentation and drying.

                                                    Drying - see Firing.

                                                    Fermentation - the tea trade term for the third stage of tea manufacture whereby the leaf enzymes oxidise on contact with air. The broken and crushed leaf is laid out on trays or in troughs and slowly, as oxidization takes place, changes from green to a rusty brown. For black tea, the oxidisation period can take up to four hours, for a semi-fermented tea the leaf is fired or dried before oxidation is complete. </br> </br>  Firing - the tea trade term for the drying of tea after oxidation, firing forms the fourth stage of manufacture. The oxidised leaf, or partially oxidised leaf in the case of semi-fermented tea, is tipped into an oven on a conveyor belt, which carries the fermented leaf through the oven, slowly drying it. It comes out of the oven as black tea,
                                                    or semi-green in the case of semi-fermented tea.
