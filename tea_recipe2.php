                        <h5>Ingredients -</h5>
                        <p>
                            ¼ litre (9 fl oz / cup) mango juice <br>
                            4 teaspoons black tea <br>
                            3 egg yolks <br>
                            80g (3 oz / 1/3 cup) sugar <br>
                            Grated rind of one lemon <br>
                            2 tablespoons lemon juice <br>
                            2 cl (1 ½ tablespoons) rum <br>
                            2 envelopes (27g / 1 oz) unflavoured gelatine <br>
                            ¼ litre (9 fl oz / 1 cup) whipped cream <br>
                            Grated chocolate to garnish <br>

                        </p>
                        <br>
                        <h5>Method -</h5>
                        <p>
                            Use the mango juice to make the tea. First boil the juice and brew the tea.
                            Allow it to stand for approximately 5 minutes, then strain and leave the tea to cool.
                            Beat the egg yolks with the cream until it is frothy. Add the juice, rum and lemon rind.
                            Soften the gelatine with 4 tablespoons cold water for approximately 10 minutes.
                            Warm it gently over hot water, stirring constantly until it has completely dissolved.
                            Gradually beat the gelatine into egg yolk mixture then beat in the tea.
                            Fold in the whipped cream as soon as the mixture begins to stiffen.
                            You can divide it between individual dessert dishes and refrigerate

